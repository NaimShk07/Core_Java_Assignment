package Assignment_Tops.Module_1;

public class N05 {
    public static void main(String[] args) {

        //display pattern like right angle triangle with a number
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
